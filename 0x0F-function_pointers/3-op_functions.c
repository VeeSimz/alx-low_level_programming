#include "clate.h"

/**
 * op_add - adds integers
 * @a: first num
 * @b: second num
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts integers
 * @a: first num
 * @b: second num
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies integers
 * @a: first num
 * @b: second num
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides integers
 * @a: first num
 * @b: second num
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a - b);
}

/**
 * op_mod - returns modulus of integers
 * @a: first num
 * @b: second num
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
